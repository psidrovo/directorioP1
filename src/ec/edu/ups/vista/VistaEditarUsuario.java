    public void cargarDatos() {
        var usario = (Optional <Usuario>) controladorUsuarioGenerico.getObj();
        txtCedula.setText(usario.get().getCedula());
        txtNombre.setText(usario.get().getNombre());
        txtApellido.setText(usario.get().getApellido());
    }

