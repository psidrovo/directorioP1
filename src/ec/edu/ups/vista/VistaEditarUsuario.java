    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        if (txtNombre.getText().equals("") || txtApellido.getText().equals("") || txtCedula.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "LLENE TODOS LOS CAMPOS", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            var us = (Optional<Usuario>) controladorUsuarioGenerico.getObj();
            controladorUsuarioGenerico.update(new Usuario(txtCedula.getText(), txtNombre.getText(), txtApellido.getText(), us.get().getDirectorio()));
            vistaPrincipal.getBienvenido().setText("BIENVENIDO " + txtNombre.getText().toUpperCase() + " " + txtApellido.getText().toUpperCase());
            this.setVisible(false);
            JOptionPane.showMessageDialog(null, "DATOS ACTUALIZADOS", "DATOS", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_btGuardarActionPerformed

